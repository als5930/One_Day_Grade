package com.biz.grade.vo;

public class StudentVO {

	private String num;
	private String name;
	private String dapt;
	private int grade;
	private String tel;
	
	
	public String getDapt() {
		return dapt;
	}
	public void setDapt(String dapt) {
		this.dapt = dapt;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
	
	
}
